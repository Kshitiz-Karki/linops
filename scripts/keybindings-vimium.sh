#!/bin/bash

bindings=(
  "[navigation]   -   j / PgUp      ’üĪ      Scroll down"
  "[navigation]   -   k / PgDn      ’üĪ      Scroll up"
  "[navigation]   -   gg            ’üĪ      Scroll to the top of the page"
  "[navigation]   -   G             ’üĪ      Scroll to the bottom of the page"
  "[navigation]   -   d             ’üĪ      Scroll a half page down"
  "[navigation]   -   u             ’üĪ      Scroll a half page up"
  "[navigation]   -   r             ’üĪ      Reload the page"
  "[navigation]   -   gi            ’üĪ      Focus the first text input on the page"
  "[navigation]   -   f             ’üĪ      Open a link in the current tab"
  "[navigation]   -   F             ’üĪ      Open a link in a new tab"
  "[find]         -   /             ’üĪ      Enter find mode"
  "[find]         -   n             ’üĪ      Cycle forward to the next find match"
  "[find]         -   N             ’üĪ      Cycle backward to the previous find match"
  "[vomnibar]     -   o             ’üĪ      Open URL, bookmark or history entry"
  "[vomnibar]     -   O             ’üĪ      Open URL, bookmark or history entry in a new tab"
  "[vomnibar]     -   b             ’üĪ      Open a bookmark"
  "[vomnibar]     -   B             ’üĪ      Open a bookmark in a new tab"
  "[vomnibar]     -   T             ’üĪ      Search through your open tabs"
  "[history]      -   H             ’üĪ      Go back in history"
  "[history]      -   L             ’üĪ      Go forward in history"
  "[tabs]         -   t             ’üĪ      Create new tab"
  "[tabs]         -   J             ’üĪ      Go one tab left"
  "[tabs]         -   K             ’üĪ      Go one tab right"
  "[tabs]         -   g0            ’üĪ      Go to the first tab"
  "[tabs]         -   g$            ’üĪ      Go to the last tab"
  "[tabs]         -   x             ’üĪ      Close current tab"
  "[tabs]         -   X             ’üĪ      Restore closed tab"
  "[help]         -   ?             ’üĪ      Show vimium keybinds and more"
)

# Pipe array into rofi in dmenu mode
printf '%s\n' "${bindings[@]}" | rofi -dmenu -p "Search" -theme-str "window { width: 85%; }"
